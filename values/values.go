package main

import "fmt"

func main() {
  // String operations
  fmt.Println("go"+"lang")

  // String number concatenation
  fmt.Println("1+1=", 1+1)
  // Float numbers
  fmt.Println("7.0/3.0=", 7.0/3.0)

  // Booleans

  fmt.Println(true && false)
  fmt.Println(true || false)
  fmt.Println(!true)

}
